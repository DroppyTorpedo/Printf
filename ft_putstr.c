#include "ft_printf.h"

void	ft_putstr(char *s, int *count)
{
	int	i;

	if (s == NULL)
		s = "(null)";
	i = 0;
	while (s[i])
	{
		write(1, &s[i], 1);
		(*count)++;
		i++;
	}
}
